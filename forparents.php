    <h2>First Aid Cabinet</h2> 

    <p>The first aid kit is kept in the Main office in a locked cabinet. It is checked weekly by assigned staff to assure familiarity with the contents at all times with both prescription and non-prescription medications. These checks will also provide screening for expiration dates of the contents as well as the need for replacement of items after use. First Aid Bags are located within each classroom and contain basic first aid materials, epi-pens, and inhalers. They are stored out of the reach of children and are readily available for use at outdoor times, hikes, and field trips. These bags are located by each classroom’s allergy list and are hung on the outside of cabinets out of reach for quick access.</p>


    <h2>Weather Closings</h2>
    <p>During the winter months, it may be necessary because of a heavy snowstorm to delay our opening time to allow for some clearing of the parking lot and walkways or to close early for safer travel. Kids-A-Lot makes every attempt to remain open throughout storm days. We take into account forecasted weather, sate advisories, and the safety of travel for parent’s, children, and staff.</p>

